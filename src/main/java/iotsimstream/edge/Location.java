package iotsimstream.edge;

/**
 * This class represents the location of edge, IoT devices
 * 
 * @author Gursharn Soni
 */
public class Location {

	public double x;
	public double y;
	public double z;

	public Location(double x, double y, double z) {
		this.x = x;
		this.y = y;
		this.z = z;

	}

}
